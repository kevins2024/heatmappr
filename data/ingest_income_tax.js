async function run() {
  // Flatten all brackets into a single array for bulk insert
  const allRows = [];
  for (const [stateFips, statuses] of Object.entries(BRACKETS)) {
    for (const [status, brackets] of Object.entries(statuses)) {
      const filing = status === 'single' ? 'single' : 'joint';
      for (const [min, max, rate] of brackets) {
        allRows.push([stateFips, filing, min, max, rate]);
      }
    }
  }

  const client = await pool.connect();
  try {
    await client.query('DELETE FROM state_income_tax');

    const CHUNK = 200;
    for (let i = 0; i < allRows.length; i += CHUNK) {
      const chunk = allRows.slice(i, i + CHUNK);
      const values = chunk.map((_, j) => {
        const b = j * 5;
        return `($${b+1},$${b+2},$${b+3},$${b+4},$${b+5})`;
      });
      await client.query(
        `INSERT INTO state_income_tax (state_fips, filing_status, bracket_min, bracket_max, rate) VALUES ${values.join(',')}`,
        chunk.flat()
      );
    }

    console.log(`Inserted ${allRows.length} income tax bracket rows for ${Object.keys(BRACKETS).length} states.`);
  } catch (e) {
    throw e;
  } finally {
    client.release();
  }
  await pool.end();
}

run().catch(e => { console.error(e); process.exit(1); });
